#! /bin/bash

mkdir -p /tmp/dust

cd /tmp/dust
wget https://api.github.com/repos/bootandy/dust/tags
VERSION=$(cat tags | grep name | head -1 | sed 's/.*"\(v.*\)".*/\1/g')
URL=https://github.com/bootandy/dust/releases/download/${VERSION}/dust-${VERSION}-x86_64-unknown-linux-gnu.tar.gz

echo "-----------------------------------------------------------------------------------------------------------------------------------"
echo "-----------------------------------------------------------------------------------------------------------------------------------"
cat tags 
echo "-----------------------------------------------------------------------------------------------------------------------------------"
cat tags | grep name 
echo "-----------------------------------------------------------------------------------------------------------------------------------"
cat tags | grep name | head -1 
echo "-----------------------------------------------------------------------------------------------------------------------------------"
cat tags | grep name | head -1 | sed 's/.*"\(v.*\)".*/\1/g'
echo "-----------------------------------------------------------------------------------------------------------------------------------"
echo "-----------------------------------------------------------------------------------------------------------------------------------"

echo "[[[ ${VERSION} ]]]"
echo "<<< ${URL} >>>"
curl -fL $URL -o dust.tar.gz

tar -xf dust.tar.gz
rm *gz
cd du*

mv dust /usr/bin

cd /
rm -rf /tmp/dust
